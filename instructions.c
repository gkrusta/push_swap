#include "list.h"

/* swap first 2 elements ( sa || sb || ss ) */
int	swap(t_list	**lst)
{
	t_list	*head;
	int		tmp_value;
	int		tmp_index;

	if (ft_lstsize(*lst) < 2)
		return (-1);
	head = *lst;
	tmp_value = head->value;
	tmp_index = head->index;
	head->value = head->next->value;
	head->index = head->next->index;
	head->next->value = tmp_value;
	head->next->index = tmp_index;
	return (0);
}
